// Math Methods and Properties
console.log(Math.PI); // 3.141592653589793: the ratio of the circumference of a circle to its diameter
console.log(Math.trunc(Math.PI)); // 3: remove the decimal part of a number
console.log(Math.floor(Math.PI)); // 3: round down to the nearest integer
console.log(Math.pow(5, 2)); // 25: 5 raised to the power of 2
console.log(Math.min(2, 0.5, 9)); // 0.5: the smallest number
console.log(Math.max(2, 0.5, 9)); // 9: the largest number
console.log(Math.random()); // random number between 0 and 1. Inclusive of 0, exclusive of 1

